def suma(n1,n2):
	"""Regresa el resultado de la suma de dos numeros"""
	return n1+n2
def resta(n1,n2):
	"""Regresa el resultado de la resta de dos numeros"""
	return n1-n2
def multiplicacion(n1,n2):
	"""Regresa el resultado de la multiplicacion de dos numeros"""
	return n1*n2
def division(n1,n2):
	"""Regresa el resultado de la division de dos numeros"""
	return n1/n2